// Числа
// Обьект Number, Math
const int = 42
const string = '42'
console.log(Number.parseInt(string) + int) // преобразование данных в интенжер

const float = 42.2
const string2 = '42.2'
console.log(Number.parseFloat(string2) + float)

console.log(Math.sqrt(25)) // квадратный корень
console.log(Math.pow(2, 3)) // возведение в степень
console.log(Math.abs(-20)) // модуль числа
console.log(Math.max(20, 1, 100, -110, 50)) // показывает максимальное число
console.log(Math.min(20, 1, 100, -110, 50)) // показывает минимальное число
console.log(Math.floor(4.9))
console.log(Math.ceil(4.9))
console.log(Math.round(4.9))
console.log(Math.trunc(4.9))
console.log(Math.random())


